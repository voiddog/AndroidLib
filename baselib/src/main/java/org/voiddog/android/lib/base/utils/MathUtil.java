public class MathUtil {

    public static final double EPS = 1e-6;

    public static int sgn(double v) {
        return sgn(v, EPS);
    }

    public static int sgn(double v, double eps) {
        return (v > eps ? 1 : 0) - (v < -eps ? 1 : 0);
    }
}
